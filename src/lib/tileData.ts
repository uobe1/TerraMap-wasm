// 按分类组织方块
export const tilesByCategory = tiles.reduce((acc, tile) => {
  if (!acc[tile.category]) {
    acc[tile.category] = [];
  }
  acc[tile.category].push(tile);
  return acc;
}, {} as Record<string, TileData[]>);

// 按 ID 查找方块
export const getTileById = (id: number): TileData | undefined => {
  return tiles.find(tile => tile.id === id);
};

// 按名称查找方块
export const getTileByName = (name: string): TileData | undefined => {
  return tiles.find(tile => tile.name === name);
};